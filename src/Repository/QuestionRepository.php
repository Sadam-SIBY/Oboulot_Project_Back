    /**
     * Retrieve all question order by exercise
     *
     * @return void
     */
    public function findAllOrderExerciseTitleAsc()
    {
        $sql = "SELECT question.*, exercise.title
                FROM question
                INNER JOIN exercise
                ON question.exercise_id = exercise.id
                ORDER BY exercise.title, question.number";
        $conn = $this->getEntityManager()->getConnection();
        $resultSet = $conn->executeQuery($sql);
        return $resultSet->fetchAllAssociative();
    }

