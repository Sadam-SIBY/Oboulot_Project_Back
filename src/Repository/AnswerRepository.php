    /**
     * Retrieve all answers by user
     *
     * @return array
     */
    public function findAllAnswerByUser($id)
    {
        $sql = "SELECT *
                FROM `answer`
                WHERE user_id = $id";
        $conn = $this->getEntityManager()->getConnection();
        $resultSet = $conn->executeQuery($sql);
        return $resultSet->fetchAllAssociative();
    }

